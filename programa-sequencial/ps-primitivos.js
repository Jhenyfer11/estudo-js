//Tipos primitivos

//NUMBER usado para numeros

const preco = 19.9; // preço com decimal
console.log(typeof preco);

//STRING usado para texto
const nome ="Lucas"
console.log (typeof nome);

// BOOLEAN verdadeiro ou falso
const ativo = true
console.log(typeof ativo);

//NUULL ausência intencional de valor
const vazio = null
console.log(typeof vazio);

//UNDEFINNED  variavel criada mas sem valor
let indefinido;
console.log(typeof indefinido);

//BIGGIN NUMEROS MUITO GRANDES (terminam com n)
